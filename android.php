<h1>Zeldesque: the Android app (ALPHA!)</h1>

<p>
  This is very alpha! It's not meant to be a good Android app quite yet (my priorities are currently elsewhere.)
</p>

<p>
  I've only tested this with my Galaxy Nexus, and the game is currently hard-coded to use a resolution of 598x360
  if it detects that it's being run on a mobile device, so unless you have a Galaxy Nexus, this probably isn't going to work very well!
</p>

<p>
  This app is basically a ten-line web browser which loads the game from http://chriswa.com/ so that you can play fullscreen. This means that:
  <ul>
    <li>you'll need an internet connection to play (the whole game is downloaded each time you start it)</li>
    <li>audio doesn't work (the Android WebView component doesn't support HTML5 Audio yet)</li>
  </ul>
</p>

<p>
  It's not on Google Play. It's an APK, which means it's a little bit of work to install:
  <ol>
    <li>On your Android, navigate to Menu -> Settings -> Applications and check the box marked "Unknown Sources"</li>
    <li>Download the <a href="Zeldesque.apk">Zeldesque.APK</a> file</li>
    <li>Copy the APK file onto your Android via USB (directly in the root of the filesystem works fine)</li>
    <li>Use an <a target="_blank" href="https://play.google.com/store/apps/details?id=mobi.infolife.installer&hl=en">installer app</a> to install it</li>
  </ol>
</p>

